char	*ft_strrev(char *str)
{
	int	l;
	int	i;
	char	a;

	i = -1;
	l = 0;
	while (str[l] != '\0')
		l++;
	while (++i < --l)
	{
		a = str[i];
		str[i] = str[l];
		str[l] = a;
	}
	return (str);
}
