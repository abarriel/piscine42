char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 65 && str[i] <= 90) || (str[i] >= 97 && str[i] <= 84))
			str[i] += 42;
		else
			return (str);
		i++;
	}
	return (str);
}
