int		ft_bouton(int i, int j, int k)
{
	if ((i >= j && i <= k) || (i >= k && i <= j))
		return (i);
	if ((j >= k && j <= i) || (j >= i && j <= k))
		return (j);
	if ((k >= j && k <= i) || (k >= i && k <= j))
		return (k);
	if (k == j && i == j)
		return (j);
}
