char	*ft_strcapitalize(char *str)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] += 32;
		}
		i++;
	}
	if (str[0] >= 97 && str[0] <= 122)
		str[0] -= 32;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 32 && str[i] <= 47) && (str[j] >= 97 && str[j] <= 122))
			str[j] -= 32;
		i++;
		j++;
	}
	return (str);
}
